/**
 * Check if category has been explained to user
 */
export const hasCategoryBeenExplained = (categoryId: string): boolean => {
  const key = `category_explained_${categoryId}`;
  return localStorage.getItem(key) === "true";
};

/**
 * Mark category as explained
 */
export const markCategoryAsExplained = (categoryId: string): void => {
  const key = `category_explained_${categoryId}`;
  localStorage.setItem(key, "true");
};
